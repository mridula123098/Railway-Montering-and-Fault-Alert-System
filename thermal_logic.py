def build_lut(scale, t_max, t_min, n_samples=256):

    sh, sw = scale.shape[:2]

    # Crop actual color bar only
    bar_start = int(sh * 0.25)
    bar_end = int(sh * 0.75)

    bar_strip = scale[bar_start:bar_end, :, :]

    rows = np.linspace(
        0,
        bar_strip.shape[0] - 1,
        n_samples,
        dtype=int
    )

    # Average colors across width
    colors = np.array(
        [bar_strip[r].mean(axis=0) for r in rows],
        dtype=np.float32
    )

    # Temperature mapping
    temps = np.linspace(
        t_max,
        t_min,
        n_samples,
        dtype=np.float32
    )

    return colors, temps


def map_pixels_to_temperature(
    image_bgr,
    scale,
    t_max,
    t_min
):

    lut_colors, lut_temps = build_lut(
        scale,
        t_max,
        t_min
    )

    h, w = image_bgr.shape[:2]

    pixels = image_bgr.reshape(-1, 3).astype(np.float32)

    temp_flat = np.zeros(
        pixels.shape[0],
        dtype=np.float32
    )

    batch_size = 10000

    for i in range(0, pixels.shape[0], batch_size):

        batch = pixels[i:i + batch_size]

        diff = (
            batch[:, None, :]
            -
            lut_colors[None, :, :]
        )

        dist = np.sum(diff ** 2, axis=2)

        nearest = np.argmin(dist, axis=1)

        temp_flat[i:i + batch_size] = lut_temps[nearest]

    temp_map = temp_flat.reshape(h, w)

    return temp_map


