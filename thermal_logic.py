# WIRE SEGMENTATION
# =========================================================

def segment_wire_and_compute_delta_t(
    temp_map,
    t_max_scale,
    t_min_scale,
    color_img
):

    h, w = temp_map.shape

    mid_thresh = (
        t_max_scale + t_min_scale
    ) / 2

    roi_mask = np.zeros((h, w), dtype=np.uint8)

    roi_mask[40:h - 40, 160:w - 80] = 1

    above_mid = (
        (temp_map >= mid_thresh)
        &
        (roi_mask == 1)
    ).astype(np.uint8)

    num_labels, labels, stats, _ = cv2.connectedComponentsWithStats(
        above_mid,
        connectivity=8
    )

    wire_mask = np.zeros((h, w), dtype=np.uint8)

    for i in range(1, num_labels):

        area = stats[i, cv2.CC_STAT_AREA]
        bw = stats[i, cv2.CC_STAT_WIDTH]
        bh = stats[i, cv2.CC_STAT_HEIGHT]

        if area < 30:
            continue

        aspect = max(bw, bh) / max(min(bw, bh), 1)

        solidity = area / max(bw * bh, 1)

        is_ui = solidity > 0.50

        is_blob = (
            area > 1500
            and
            solidity > 0.45
            and
            aspect < 3.0
        )

        is_wire = (
            (aspect >= 3.0 or solidity < 0.35)
            and
            area > 30
        )

        if is_wire and not is_ui and not is_blob:

            wire_mask[labels == i] = 1

    # Morphological cleanup
    kernel = cv2.getStructuringElement(
        cv2.MORPH_ELLIPSE,
        (3, 3)
    )

    wire_mask = cv2.morphologyEx(
        wire_mask,
        cv2.MORPH_OPEN,
        kernel
    )

    wire_mask = cv2.morphologyEx(
        wire_mask,
        cv2.MORPH_CLOSE,
        kernel
    )

    if wire_mask.sum() == 0:

        return {
            "wire_t_max": None,
            "wire_t_min": None,
            "delta_t": None,
            "alert": "No wire detected",
            "wire_mask": wire_mask
        }

    wire_temps = temp_map[wire_mask == 1]

    wire_t_max = float(
        np.percentile(wire_temps, 99)
    )

    wire_t_min = float(
        np.percentile(wire_temps, 5)
    )

    delta_t = wire_t_max - wire_t_min

    # Alert logic
    if delta_t > 20:

        alert = "CRITICAL - Attend within 24 hrs"

    elif delta_t > 10:

        alert = "WARNING - Attend within 10 days"

    elif delta_t > 5:

        alert = "MONITOR - Attend within 1 month"

    else:

        alert = "NORMAL"

    return {
        "wire_t_max": wire_t_max,
        "wire_t_min": wire_t_min,
        "delta_t": delta_t,
        "alert": alert,
        "wire_mask": wire_mask
    }


