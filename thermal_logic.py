def process_image(image_path):

    # Load image
    color_img = cv2.imread(image_path)

    # Check image
    if color_img is None:

        raise ValueError(
            f"Failed to load image at {image_path}"
        )

    # =====================================================
    # EXTRACT SCALE
    # =====================================================

    h, w = color_img.shape[:2]

    scale = color_img[
        :,
        int(w * 0.94):int(w * 0.98)
    ]

    # =====================================================
    # CROP TOP/BOTTOM SCALE VALUES
    # =====================================================

    sh, sw = scale.shape[:2]

    top = scale[
        int(sh * 0.13):int(sh * 0.23),
        :
    ]

    bottom = scale[
        int(sh * 0.78):int(sh * 0.88),
        :
    ]

    # =====================================================
    # OCR SCALE TEMPERATURES
    # =====================================================

    t_max_abs = crop_to_temp(top)
    t_min_abs = crop_to_temp(bottom)

    top_is_negative = has_minus_sign(top)
    bot_is_negative = has_minus_sign(bottom)

    t_max = (
        -t_max_abs
        if (top_is_negative and t_max_abs)
        else t_max_abs
    )

    t_min = (
        -t_min_abs
        if (bot_is_negative and t_min_abs)
        else t_min_abs
    )

    # =====================================================
    # SANITY CHECK
    # =====================================================

    if t_max is not None and t_min is not None:

        if t_max < t_min:

            if top_is_negative and not bot_is_negative:

                t_max = t_max_abs

            elif bot_is_negative and not top_is_negative:

                t_min = t_min_abs

            else:

                t_max, t_min = (
                    max(t_max_abs, t_min_abs),
                    min(t_max_abs, t_min_abs)
                )

    # =====================================================
    # CREATE TEMPERATURE MAP
    # =====================================================

    temp_map = map_pixels_to_temperature(
        color_img,
        scale,
        t_max,
        t_min
    )

    # =====================================================
    # SEGMENT WIRE + COMPUTE DELTA T
    # =====================================================

    result = segment_wire_and_compute_delta_t(
        temp_map,
        t_max,
        t_min,
        color_img
    )

    # =====================================================
    # RETURN RESULTS
    # =====================================================

    return {
        "scale_t_max": t_max,
        "scale_t_min": t_min,
        "max_temp": result["wire_t_max"],
        "min_temp": result["wire_t_min"],
        "delta": result["delta_t"],
        "status": result["alert"],
        "temp_map": temp_map,
        "wire_mask": result["wire_mask"]
    }

