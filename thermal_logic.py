def crop_to_temp(crop_bgr):

    # Upscale image for better OCR
    big = cv2.resize(
        crop_bgr,
        (crop_bgr.shape[1] * 8, crop_bgr.shape[0] * 8),
        interpolation=cv2.INTER_LANCZOS4
    )

    # Convert to grayscale
    gray = cv2.cvtColor(big, cv2.COLOR_BGR2GRAY)

    # Threshold
    _, th = cv2.threshold(
        gray,
        150,
        255,
        cv2.THRESH_BINARY
    )

    best = None

    for psm in [7, 8, 13]:

        cfg = f"--psm {psm} -c tessedit_char_whitelist=0123456789."

        txt = pytesseract.image_to_string(
            Image.fromarray(th),
            config=cfg
        ).strip()

        nums = re.findall(r"\d+\.?\d*", txt)

        if nums and best is None:
            best = float(nums[0])

    return best


def has_minus_sign(crop_bgr):

    gray = cv2.cvtColor(crop_bgr, cv2.COLOR_BGR2GRAY)

    bright_rows = [
        r for r in range(gray.shape[0])
        if gray[r].mean() > 100
    ]

    if not bright_rows:
        return False

    box_start = bright_rows[0]
    box_end = bright_rows[-1]

    above = gray[:box_start, :]
    below = gray[box_end + 1:, :]

    def minus_in_region(region):

        if region.shape[0] == 0:
            return False

        for row in range(region.shape[0]):

            row_max = region[row].max()
            row_mean = region[row].mean()

            if row_mean < 80 and row_max > 50:
                return True

        return False

    return (
        minus_in_region(above)
        or
        minus_in_region(below)
    )


